export function toTraditional(text: string): string {
  if (!text) return text
  let result = text
  // First do multi-char replacements (longer strings first)
  const multi: [string, string][] = [
    ['當歸', '当归'], ['黃耆', '黄芪'], ['黃芩', '黄芩'], ['黃連', '黄连'],
    ['蒼术', '苍术'], ['白术', '白术'], ['蘇葉', '苏叶'], ['蘇子', '苏子'],
    ['陳皮', '陈皮'], ['麥冬', '麦冬'], ['麥芽', '麦芽'], ['蘆根', '芦根'],
    ['香附', '香附'], ['龍骨', '龙骨'], ['龜板', '龟板'], ['鱉甲', '鳖甲'],
    ['烏藥', '乌药'], ['葛根湯', '葛根汤'], ['麻黃湯', '麻黄汤'],
    ['桂枝湯', '桂枝汤'], ['銀翹散', '银翘散'], ['龍膽湯', '龙胆汤'],
    ['防風通聖', '防风通圣'], ['防風通聖散', '防风通圣散'],
  ]
  // Reverse: SC -> TC
  const scMulti: [string, string][] = multi.map(([tc, sc]) => [sc, tc])
  for (const [s, t] of scMulti) {
    result = result.split(s).join(t)
  }
  // Then single-char replacements
  const singleChars: [string, string][] = [
    ['風','风'],['藥','药'],['湯','汤'],['聖','圣'],['當','当'],['歸','归'],
    ['黃','黄'],['連','连'],['術','术'],['處','处'],['號','号'],['經','经'],
    ['發','发'],['後','后'],['異','异'],['員','员'],['對','对'],['進','进'],
    ['遠','远'],['還','还'],['環','环'],['報','报'],['學','学'],['線','线'],
    ['記','记'],['區','区'],['蘇','苏'],['鐵','铁'],['鹽','盐'],['點','点'],
    ['龍','龙'],['龜','龟'],['鱉','鳖'],['烏','乌'],['齊','齐'],['變','变'],
    ['顯','显'],['擔','担'],['選','选'],['錢','钱'],['長','长'],['開','开'],
    ['門','门'],['間','间'],['關','关'],['書','书'],['國','国'],['機','机'],
    ['義','义'],['總','总'],['業','业'],['萬','万'],['廣','广'],['東','东'],
    ['來','来'],['見','见'],['強','强'],['應','应'],['樣','样'],['從','从'],
    ['種','种'],['眾','众'],['徹','彻'],['極','极'],['觀','观'],['認','认'],
    ['體','体'],['讓','让'],['論','论'],['話','话'],['動','动'],['電','电'],
    ['實','实'],['華','华'],['產','产'],['會','会'],['網','网'],['車','车'],
    ['無','无'],['與','与'],['為','为'],['時','时'],['這','这'],['個','个'],
    ['們','们'],['並','并'],['裡','里'],['質','质'],['幾','几'],
  ]
  const tcChars: [string, string][] = singleChars.map(([tc, sc]) => [sc, tc])
  for (const [s, t] of tcChars) {
    result = result.split(s).join(t)
  }
  return result
}